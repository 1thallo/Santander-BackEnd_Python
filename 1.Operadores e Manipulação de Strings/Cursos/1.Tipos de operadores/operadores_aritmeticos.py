# Adição
print(1+1) # 2

# Subtração
print(10 - 2) # 8

# Multiplicação
print(4 * 3) # 12

# Divisão
print(12/3) # 4.0

# Divisão inteira
print(12 // 3) # 4

# Módulo
print(10 % 3) # 1

# Exponenciação
print (2 ** 3) # 8

# Precedências matemáticas
print(10 - 5 * 2) # 0

print((10 - 5) * 2) # 10

print(10 ** 2 * 2) # 200

print(10 ** (2 * 2)) # 10000

print(10 / 2 * 4) # 20.0

# Prática com variáveis
produto1 = 10
produto2 = 20

print(produto1 + produto2) # 30
print(produto1 - produto2) # -10
print(produto1 / produto2) # 0.5
print(produto1 // produto2) # 0
print(produto1 * produto2) # 200
print(produto1 ** produto2) # 100000000000000000000
print(produto1 % produto2) # 10

