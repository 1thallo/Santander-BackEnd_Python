# Atribuição simples
saldo = 500

print(saldo) # 500

# Com adição
saldo = 500
saldo += 200

print(saldo) # 700

# Subtração
saldo = 500
saldo -= 100

print(saldo) # 400

# Divisão
saldo = 500
saldo /= 5 # 100.0

print(saldo)

saldo = 500
saldo //= 5

print(saldo) # 100

# Módulo
saldo = 500
saldo %= 480

print(saldo) # 20

# Exponenciação
saldo = 80
saldo **= 2

print(saldo) # 6400